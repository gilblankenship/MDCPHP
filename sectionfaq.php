                <div data-bs-toggle="collapse" class="collapsed question" href="#faq1">What is a startup studio? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq1" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    <a href="https://medium.com/swlh/the-entrepreneurs-guide-to-startup-studio-models-5f81706f847" target="_blank">Dianna Lesage: </a>
                      "Startup Studios transform unique ideas into strong independent companies. These entities 
                      don’t run programs as accelerators do and they don’t have cohorts like incubators do. The 
                      secret sauce of a Startup Studio (also called a Venture Builder or a Startup Factory) is 
                      the way they work on startup ideas. The industry term for this is “operational model” but 
                      there is no reason to use vague language to make things complicated. They do this by finding 
                      or developing great ideas, using technology and funding to build MVPs, and going through the 
                      process of validating these MVPs in the real world. 
                      If all goes well, the Studio “spins off” the new venture- meaning the Studio recognizes this 
                      new company they’ve developed could be huge and now they need to find some people to run it 
                      as an independent startup venture.
                      Once they’ve recruited a team to come run the startup, the Studio team (who built the startup 
                      from scratch) hands off the venture to the new team and gets back to work on the next great 
                      startup idea.The Startup Studio model is very different from accelerators, incubators, and 
                      other innovation programs. Instead of using financial and physical resources 
                      to help entrepreneurs accelerate ideas, a Startup Studio uses its own internal team to do it."
                  </p>
                </div>
